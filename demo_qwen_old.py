# --- Args normalizer for schema compliance ---
POS_INT_RE = re.compile(r"^[1-9]\d*$")

def normalize_args_for_schema(raw_args: Dict[str, Any]) -> Dict[str, Any]:
    args = dict(raw_args) if isinstance(raw_args, dict) else {}

    # Map Total_product -> total_product
    if "Total_product" in args and "total_product" not in args:
        args["total_product"] = args.pop("Total_product")

    def _clean_list(lst):
        if not isinstance(lst, list):
            return []
        keep = []
        for p in lst:
            if isinstance(p, str):
                ps = product_sanitize(p)
                if ps:
                    keep.append(ps)
        return keep

    out: Dict[str, Any] = {}

    if "products" in args:
        prods = _clean_list(args.get("products"))
        if len(prods) >= 2:
            out = {"products": prods, "total_product": str(len(prods))}
        elif len(prods) == 1:
            out = {"product": prods[0], "total_product": "1"}
        else:
            out = {}
    elif "product" in args and isinstance(args["product"], str):
        prod = product_sanitize(args["product"])
        if prod:
            out = {"product": prod, "total_product": "1"}
        else:
            out = {}
    else:
        out = {}

    if "total_product" in out:
        tp = str(out["total_product"])
        if not POS_INT_RE.fullmatch(tp):
            if "products" in out:
                out["total_product"] = str(max(1, len(out["products"])))
            elif "product" in out:
                out["total_product"] = "1"
            else:
                out.pop("total_product", None)

    if "products" in out:
        out = {"products": out["products"], "total_product": out.get("total_product", str(len(out["products"])))}
    elif "product" in out:
        out = {"product": out["product"], "total_product": out.get("total_product", "1")}
    else:
        out = {}

    return out

# ---------- JSON extractor ----------
def extract_json_strict(text: str, begin="<<BEGIN_JSON>>", end="<<END_JSON>>") -> Optional[str]:
    if not isinstance(text, str): return None
    bi = text.rfind(begin)
    frag = text[bi+len(begin):] if bi != -1 else text
    ei = frag.find(end) if bi != -1 else -1
    if ei != -1: frag = frag[:ei]
    start = frag.find("{")
    if start == -1: return None
    buf = []; bal = 0; started = False
    for ch in frag[start:]:
        buf.append(ch)
        if ch == "{":
            bal += 1; started = True
        elif ch == "}":
            bal -= 1
            if started and bal == 0:
                s = "".join(buf)
                try:
                    json.loads(s)
                    return s
                except Exception:
                    return None
    return None

# ==== detect location kết hợp NER + regex + gazetteer ====
def detect_location_dual(q: str) -> Tuple[Optional[str], Optional[str]]:
    candidates: List[str] = []
    candidates += _ner_vi_locations(q)
    candidates += _ner_en_locations(q)
    m = re.search(r"(?i)\b(?:in|ở|tai|tại)\s+([A-Za-zÀ-ỹ0-9\-\s]+)", q)
    if m:
        candidates.append(m.group(1).strip(" ,.!?"))
    qn = _basic_norm(q)
    for k in LOC_SYNONYMS.keys():
        if k in qn:
            candidates.append(k)

    seen = set()
    for cand in candidates:
        ctext = cand.strip()
        if not ctext or ctext.lower() in seen:
            continue
        seen.add(ctext.lower())
        canon = canon_location_from_string(ctext)
        if canon:
            return ctext, canon

    if candidates:
        return candidates[0].strip(), None
    return None, None

# ---------- Stopping on token pattern ----------
class StopOnTokenPattern(StoppingCriteria):
    def __init__(self, tokenizer, pattern="<<END_JSON>>"):
        self.ids = tokenizer(pattern, add_special_tokens=False).input_ids
        self.k = len(self.ids)
    def __call__(self, input_ids, scores, **kwargs):
        seq = input_ids[0].tolist()
        return len(seq) >= self.k and seq[-self.k:] == self.ids

# ---------- NoBadWords helper ----------
def make_nbw_processor(tokenizer, bad_strings: List[str]):
    bad_ids = []
    for s in bad_strings:
        try:
            ids = tokenizer(s, add_special_tokens=False).input_ids
            if ids: bad_ids.append(ids)
        except Exception:
            pass
    from transformers.generation.logits_process import NoBadWordsLogitsProcessor
    try:
        return NoBadWordsLogitsProcessor(bad_words_ids=bad_ids, eos_token_id=tokenizer.eos_token_id)
    except TypeError:
        return NoBadWordsLogitsProcessor(bad_ids)

