def _ner_vi_locations(text: str) -> List[str]:
    if vn_ner is None or not isinstance(text, str):
        return []
    spans, buf = [], []
    try:
        for w, pos, chunk, tag in vn_ner(text):
            if isinstance(tag, str) and tag.startswith("B-LOC"):
                if buf: spans.append(" ".join(buf)); buf = []
                buf = [w]
            elif isinstance(tag, str) and tag.startswith("I-LOC"):
                if buf: buf.append(w)
            else:
                if buf: spans.append(" ".join(buf)); buf = []
        if buf: spans.append(" ".join(buf))
    except Exception:
        return []
    return spans

