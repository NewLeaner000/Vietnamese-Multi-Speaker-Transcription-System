def has_reviews_intent(text: str) -> bool:
    if not isinstance(text, str) or not text.strip():
        return False
    qn = unicodedata.normalize("NFD", text)
    qn = "".join(ch for ch in qn if not unicodedata.combining(ch))
    qn = re.sub(r"[^a-zA-Z0-9\s\-\+_/\.⭐★]", " ", qn).lower().strip()
    for kw in INTENT_KEYWORDS["reviews"]:
        if kw in qn:
            return True
    for pat in REVIEW_PATTERNS:
        if re.search(pat, qn, flags=re.I):
            return True
    return False

