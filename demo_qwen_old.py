# ---------- Model loader ----------
def load_tokenizer_and_model(base_model: str, ckpt_dir: Path, deterministic: bool = True):
    try:
        tok = AutoTokenizer.from_pretrained(str(ckpt_dir), use_fast=False, trust_remote_code=True)
    except Exception:
        tok = AutoTokenizer.from_pretrained(base_model, use_fast=False, trust_remote_code=True)
    if tok.pad_token is None:
        tok.pad_token = tok.eos_token

    compute_dtype = torch.bfloat16 if torch.cuda.is_bf16_supported() else torch.float16
    try:
        bnb_cfg = BitsAndBytesConfig(load_in_4bit=True, bnb_4bit_quant_type="nf4",
                                     bnb_4bit_use_double_quant=True, bnb_4bit_compute_dtype=compute_dtype)
        base = AutoModelForCausalLM.from_pretrained(base_model, trust_remote_code=True,
                                                    quantization_config=bnb_cfg, device_map="auto")
    except Exception:
        base = AutoModelForCausalLM.from_pretrained(base_model, trust_remote_code=True)
        if torch.cuda.is_available(): base.to("cuda:0")

    model = PeftModel.from_pretrained(base, str(ckpt_dir))
    model.eval()

    if deterministic:
        torch.manual_seed(0)

    return tok, model

