class EvaluatorGuarded:
    def __init__(self, base_model: str, ckpt_dir: Path, policy_path: Path,
                 max_new_tokens: int = 160, use_outline: bool = True, deterministic: bool = True,
                 print_model_reasoning: bool = False):   # NEW
        self.base_model = base_model
        self.ckpt_dir   = Path(ckpt_dir)
        self.max_new_tokens = int(max_new_tokens)
        self.use_outline = bool(use_outline)
        self.print_model_reasoning = bool(print_model_reasoning)  # NEW

        self.tok, self.model = load_tokenizer_and_model(base_model, self.ckpt_dir, deterministic=deterministic)
        self.pb = PromptBuilderUploaded(policy_path)
        self._end_stopper = StoppingCriteriaList([StopOnTokenPattern(self.tok, "<<END_JSON>>")])

    @torch.no_grad()
    def _decode_guarded(self, prompt: str, max_new_tokens: Optional[int] = None) -> Tuple[Optional[str], str]:
        if max_new_tokens is None: max_new_tokens = self.max_new_tokens
        enc = self.tok(prompt, return_tensors="pt", truncation=True, max_length=1800).to(self.model.device)
        nbw = make_nbw_processor(self.tok, BAD_STRINGS)
        lp = LogitsProcessorList([nbw])
        out = self.model.generate(
            **enc,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            logits_processor=lp,
            stopping_criteria=self._end_stopper,
            pad_token_id=self.tok.pad_token_id,
            eos_token_id=self.tok.eos_token_id,
            use_cache=True
        )
        txt = self.tok.decode(out[0], skip_special_tokens=True)
        js_text = extract_json_strict(txt, begin="<<BEGIN_JSON>>", end="<<END_JSON>>")
        return js_text, txt

    def _intent_from_query(self, q: str) -> Dict[str, bool]:
        qn = _basic_norm(q)
        def has(keys): return any(k in qn for k in keys)
        return {
            "stock": has(INTENT_KEYWORDS["stock"]),
            "price": has(INTENT_KEYWORDS["price"]),
            "policy": has(INTENT_KEYWORDS["policy"]),
            "spec": has(INTENT_KEYWORDS["spec"]),
            "reviews": (has(INTENT_KEYWORDS["reviews"]) or has_reviews_intent(q)),
            "recommend": has(INTENT_KEYWORDS["recommend"]),
            "compare": has(INTENT_KEYWORDS["compare"]),
        }

    def _extract_product_from_query(self, query: str) -> Optional[str]:
        if not isinstance(query, str) or not query.strip():
            return None
        s = _strip_role_loc(query)
        s = _expand_phone_aliases(s)
        s = re.split(r'(?i)\b(?:vs|versus)\b', s)[0]
        s = re.sub(r'(?i)^\s*compare\s+', '', s)
        pat = r'(?i)(iphone|ipad|macbook|airpods|watch|galaxy|pixel|oneplus|oppo|reno|find|xiaomi|redmi|poco|realme|vivo|huawei|honor|asus|rog|nokia|tecno|infinix)\s+[A-Za-z0-9][A-Za-z0-9\s\-\+]*'
        m = re.search(pat, s, flags=re.I)
        return clean_surface_product(m.group(0)) if m else None

    def _extract_compare_products(self, query: str) -> List[str]:
        s = _strip_role_loc(query)
        s = _expand_phone_aliases(s)
        s = re.sub(r'(?i).*?\b(compare|so\s*s(á|a)nh)\b', '', s, count=1).strip() or s
        s = AND_SPLIT_RE.sub('|', s).strip(" ?.!")

        parts = [product_sanitize(p) for p in s.split('|')]
        parts = [p for p in parts if p]

        def _is_compare_like(p: str) -> bool:
            if not p: return False
            brand_pat = re.compile(
                r'(?i)\b('
                r'iphone|ipad|pad\s*air|ipad\s*air|ipad\s*mini|macbook|airpods|apple\s*watch|watch|'
                r'galaxy|pixel|oneplus|oppo|reno|find|xiaomi|redmi|poco|realme|vivo|huawei|honor|asus|rog|nokia|tecno|infinix'
                r')\b'
            )
            return bool(brand_pat.search(p))

        parts = [p for p in parts if _is_compare_like(p)]

        if len(parts) >= 2:
            uniq = []
            for p in parts:
                if p not in uniq:
                    uniq.append(p)
            return uniq[:2]
        return []

    def outline_skeleton(self, query: str) -> Dict[str, Any]:
        intents = self._intent_from_query(query)

        loc_raw, loc_canon = detect_location_dual(query)
        loc = loc_canon  # luôn ASCII hoặc None

        pair: List[str] = self._extract_compare_products(query) if intents["compare"] else []
        prod = self._extract_product_from_query(query)

        seq: List[Dict[str, Any]] = []
        def add(tool_name: str):
            t = canon_tool_name(tool_name)
            if not t: return
            step = {"tool": t, "args": {}}
            if len(pair) == 2 and t in {"check_stock","check_price","check_spec","policy_look_up_tool","get_product_reviews","recommend_product"}:
                step["args"]["products"] = pair
                step["args"]["total_product"] = "2"
            elif prod and t != "compare_all":
                step["args"]["product"] = prod
                step["args"]["total_product"] = "1"
            seq.append(step)

        if intents["stock"]: add("check_stock")
        if intents["price"]: add("check_price")
        if intents["policy"]: add("policy_look_up_tool")
        if intents["spec"]: add("check_spec")
        if intents["reviews"]: add("get_product_reviews")
        if intents["recommend"]: add("recommend_product")

        if intents["compare"] and len(pair) == 2 and not any(
            intents[k] for k in ["stock","price","policy","spec","reviews","recommend"]
        ):
            seq.append({"tool":"compare_all","args":{"products": pair, "total_product":"2"}})

        return {"reasoning": None, "location": (loc if loc else None), "tool_sequence": seq}

    # ---------- Coerce/Normalize + rescue ----------
    def coerce_to_schema(self, obj: Any) -> Dict[str, Any]:
        out: Dict[str, Any] = {"reasoning": None, "location": None, "tool_sequence": []}
        if isinstance(obj, dict):
            out["reasoning"] = None if obj.get("reasoning") is None else str(obj.get("reasoning"))
            out["location"] = obj.get("location", None)
            seq = obj.get("tool_sequence", [])
            if not isinstance(seq, list): seq = [] if seq is None else [seq]
            for step in seq:
                if isinstance(step, dict):
                    out["tool_sequence"].append({
                        "tool": (step.get("tool") or step.get("name") or ""),
                        "args": (step.get("args") or {}) if isinstance(step.get("args"), dict) else {}
                    })
        return out

    def normalize_and_validate(self, record: Dict[str, Any], query: str) -> Tuple[Dict[str, Any], Dict[str, Any]]:
        notes = {"dropped_steps": [], "fixed_steps": [], "order": "reordered_by_priority"}
        r = json.loads(json.dumps(record, ensure_ascii=False))

        # short-circuit nếu phát hiện địa danh nhưng không hỗ trợ
        raw_loc, canon_loc = detect_location_dual(query)
        if raw_loc and not canon_loc:
            unsupported = {"reasoning": f"Không hỗ trợ tại {raw_loc[:60]}", "location": None, "tool_sequence": []}
            validate_or_raise(unsupported)
            return unsupported, {"short_circuit": "unsupported_location", "raw_location": raw_loc}

        # nếu có canon_loc, set location; không thì None
        r["location"] = canon_loc if canon_loc else None  # luôn là ASCII hoặc None

        seq_in = r.get("tool_sequence") or []
        bucket = {}
        for st in seq_in:
            raw_tool = st.get("tool","")
            t = canon_tool_name(raw_tool)
            if not t:
                notes["dropped_steps"].append({"tool_raw": raw_tool, "reason":"unknown_or_disallowed"})
                continue
            args = st.get("args") or {}

            if "products" in args and "product" in args:
                args.pop("product", None)
            if "product" in args:
                args["product"] = product_sanitize(args["product"])
                args["total_product"] = "1"
            elif "products" in args and isinstance(args["products"], list):
                args["products"] = [product_sanitize(p) for p in args["products"] if isinstance(p,str) and product_sanitize(p)]
                if not args["products"]:
                    notes["dropped_steps"].append({"tool": t, "reason":"empty_products"})
                    continue
                args["total_product"] = str(len(args["products"]))
            else:
                if t != "compare_all":
                    prod2 = self._extract_product_from_query(query)
                    if prod2:
                        args = {"product": prod2, "total_product":"1"}
                    else:
                        args = {}

            if t == "compare_all":
                prods_in = args.get("products", [])
                fixed = []
                for p in prods_in:
                    if AND_SPLIT_RE.search(p):
                        parts = [product_sanitize(x) for x in AND_SPLIT_RE.split(p)]
                        fixed.extend([pp for pp in parts if pp])
                    else:
                        fixed.append(product_sanitize(p))
                fixed = [p for p in fixed if p and p.lower() not in ("user", "assistant")]
                uniq = []
                for p in fixed:
                    if p not in uniq:
                        uniq.append(p)
                if len(uniq) >= 2:
                    args["products"] = uniq[:2]
                    args["total_product"] = "2"
                else:
                    notes["dropped_steps"].append({"tool":"compare_all","reason":"need_two_products_after_sanitize"})
                    continue

                args = normalize_args_for_schema(args)
            else:
                args = normalize_args_for_schema(args)

            key = t
            if key not in bucket:
                bucket[key] = {"tool": t, "args": args}
            else:
                prev = bucket[key]["args"]
                score = lambda a: int(bool(a.get("product"))) + int(bool(a.get("products"))) + int(bool(a.get("total_product")))
                if score(args) > score(prev):
                    bucket[key]["args"] = args

        intents_check = self._intent_from_query(query)
        if intents_check["reviews"] and "get_product_reviews" not in bucket:
            prod3 = self._extract_product_from_query(query)
            bucket["get_product_reviews"] = {"tool":"get_product_reviews","args":({"product": prod3, "total_product":"1"} if prod3 else {})}
            notes.setdefault("rescued", []).append("get_product_reviews")

        ordered = [bucket[t] for t in TOOL_PRIORITY if t in bucket]
        r["tool_sequence"] = ordered
        notes["fixed_steps"] = ordered
        return r, notes

    # ---------- Pipeline ----------
    @torch.no_grad()
    def debug_pipeline(self, query: str) -> Dict[str, Any]:
        prompt = self.pb.build_inference_prompt(query)
        js_text, raw = self._decode_guarded(prompt, max_new_tokens=self.max_new_tokens)

        raw_reasoning = None  # NEW: giữ reasoning gốc
        obj_model = {}
        if js_text is not None:
            try:
                _raw_obj = json.loads(js_text)
                raw_reasoning = _raw_obj.get("reasoning", None)
            except Exception:
                pass
            try:
                obj_model = json.loads(js_text)
            except Exception:
                obj_model = {}
        obj_model = self.coerce_to_schema(obj_model)

        skel = self.outline_skeleton(query) if self.use_outline else {"reasoning": None, "location": None, "tool_sequence": []}

        seq_model = obj_model.get("tool_sequence") or []
        seq_skel  = skel.get("tool_sequence") or []
        merged = []
        seen = set()
        def score(a):
            if not isinstance(a, dict): return -1
            args = a.get("args",{}) or {}
            return int(bool(args.get("product"))) + int(bool(args.get("products"))) + int(bool(args.get("total_product")))
        for cand in seq_model + seq_skel:
            t = canon_tool_name((cand or {}).get("tool",""))
            if not t: continue
            if t not in seen:
                merged.append(cand); seen.add(t)
            else:
                for i, s in enumerate(merged):
                    if canon_tool_name(s.get("tool","")) == t and score(cand) > score(s):
                        merged[i] = cand
                        break

        obj_merged = {"reasoning": None, "location": obj_model.get("location") or skel.get("location"), "tool_sequence": merged}
        final, notes = self.normalize_and_validate(obj_merged, query)

        try:
            validate_or_raise(final)
        except Exception:
            fallback, _ = self.normalize_and_validate(skel, query)
            validate_or_raise(fallback)
            final = fallback

        return {
            "query": query,
            "decoder_used": "GUARDED_GENERATE",
            "raw_tail": (raw[-RAW_TAIL_CHARS:] if isinstance(raw, str) else ""),
            "pred_before_norm": obj_model,
            "skeleton": skel,
            "merged": obj_merged,
            "final": final,
            "notes": notes,
            "model_reasoning": raw_reasoning  # NEW
        }

    # ---------- Helper: chỉ lấy reasoning phần gen ----------
    def get_model_reasoning_only(self, query: str) -> Optional[str]:  # NEW
        info = self.debug_pipeline(query)
        return info.get("model_reasoning")

    # ---------- In-memory run ----------
    def run_on_records(self, inputs: List[Dict[str, Any]], gts: Optional[List[Dict[str, Any]]] = None,
                       print_first: int = 6) -> Tuple[List[Dict[str, Any]], Dict[str, Any]]:
        def unwrap(y):
            return y["output"] if (isinstance(y, dict) and "output" in y and isinstance(y["output"], dict)) else y

        results, tool_hist_pred, tool_hist_gt = [], {}, {}
        t0 = time.time()
        reviews_miss = []

        for i, x in enumerate(inputs):
            q = (x or {}).get("query","")
            info = self.debug_pipeline(q)
            pred = info["final"]

            rec_row = {
                "query": q,
                "pred": pred,
                "decoder": info["decoder_used"],
                "raw_tail": info["raw_tail"],
                "notes": info["notes"],
                "pred_before_norm": info["pred_before_norm"],
                "skeleton": info["skeleton"],
                "merged": info["merged"],
                "model_reasoning": info.get("model_reasoning")  # NEW
            }

            if gts is not None:
                y = unwrap(gts[i]) if i < len(gts) else {}
                gt = self.coerce_to_schema(y or {})
                gt["location"] = canon_location_from_query(q) if canon_location_from_query(q) else None

                em_strict = (json.dumps(pred, ensure_ascii=False, sort_keys=True) ==
                             json.dumps(gt,   ensure_ascii=False, sort_keys=True))
                def tool_names(obj):
                    seq = (obj or {}).get("tool_sequence") or []
                    return [canon_tool_name(s.get("tool")) for s in seq if isinstance(s, dict) and canon_tool_name(s.get("tool"))]
                same_loc = (pred or {}).get("location", None) == (gt or {}).get("location", None)
                em_logic = same_loc and (tool_names(pred) == tool_names(gt))

                ps, gs = set(tool_names(pred)), set(tool_names(gt))
                tp = len(ps & gs); fp = len(ps - gs); fn = len(gs - ps)
                prec = tp / (tp+fp) if (tp+fp)>0 else 0.0
                rec  = tp / (tp+fn) if (tp+fn)>0 else 0.0
                f1   = 2*prec*rec/(prec+rec) if (prec+rec)>0 else 0.0

                for t in tool_names(pred): tool_hist_pred[t] = tool_hist_pred.get(t,0)+1
                for t in tool_names(gt):   tool_hist_gt[t]   = tool_hist_gt.get(t,0)+1

                gt_tools = set(tool_names(gt))
                pd_tools = set(tool_names(pred))
                if "get_product_reviews" in gt_tools and "get_product_reviews" not in pd_tools:
                    intents = self._intent_from_query(q)
                    reviews_miss.append({
                        "idx": i, "query": q,
                        "intent_detected_reviews": intents["reviews"],
                        "gt_has_reviews": True, "pred_has_reviews": False
                    })

                rec_row.update({
                    "gt": gt,
                    "exact_match_strict": bool(em_strict),
                    "exact_match_logic": bool(em_logic),
                    "tool_set_scores": {"precision": prec, "recall": rec, "f1": f1},
                })

            results.append(rec_row)

            if i < int(print_first):
                print("\n" + "="*90)
                print(f"[{i}] query:", q)
                if self.print_model_reasoning:
                    print("- model_reasoning:", json.dumps(rec_row["model_reasoning"], ensure_ascii=False))  # NEW
                print("- pred(final):", json.dumps(pred, ensure_ascii=False))

        elapsed = time.time() - t0

        if gts is None:
            metrics = {
                "total": len(results),
                "elapsed_sec": elapsed,
                "tools_pred_hist": tool_hist_pred,
                "note": "No ground-truth provided → metrics vs GT not computed."
            }
        else:
            em_logic_rate = float(sum(r.get("exact_match_logic", False) for r in results)) / max(len(results),1)
            em_strict_rate = float(sum(r.get("exact_match_strict", False) for r in results)) / max(len(results),1)
            f1s = [r["tool_set_scores"]["f1"] for r in results]
            precs = [r["tool_set_scores"]["precision"] for r in results]
            recs  = [r["tool_set_scores"]["recall"] for r in results]
            metrics = {
                "total": len(results),
                "em_strict_rate": em_strict_rate,
                "em_logic_rate": em_logic_rate,
                "tool_set_precision_avg": float(np.mean(precs)) if precs else 0.0,
                "tool_set_recall_avg": float(np.mean(recs)) if recs else 0.0,
                "tool_set_f1_avg": float(np.mean(f1s)) if f1s else 0.0,
                "tools_pred": tool_hist_pred,
                "tools_gt": tool_hist_gt,
                "elapsed_sec": elapsed,
                "reviews_miss_count": len(reviews_miss)
            }

        return results, metrics

# === Cell 3: Runner — chỉ xử lí trực tiếp, không ghi file ===
def run_eval_direct(base_model: str,
                    ckpt_dir: str,
                    policy_path: str = "",
                    queries: List[str] = None,
                    print_first: int = PRINT_FIRST,
                    max_new_tokens: int = MAX_NEW_TOKENS,
                    use_outline: bool = USE_OUTLINE,
                    deterministic: bool = DETERMINISTIC,
                    gts: Optional[List[Dict[str, Any]]] = None
                   ) -> Tuple[List[Dict[str, Any]], Dict[str, Any], List[Dict[str, Any]]]:
    """
    Trả về:
      - final_pred: list JSON hợp lệ theo schema của bạn
      - metrics: dict (nếu không có GT thì chỉ có tổng quan)
      - debug_rows: list chi tiết từng mẫu (raw_tail, skeleton, merged, notes)
    """
    if queries is None:
        queries = []

    ev = EvaluatorGuarded(
        base_model=base_model,
        ckpt_dir=Path(ckpt_dir),
        policy_path=Path(policy_path) if policy_path else Path(),
        max_new_tokens=max_new_tokens,
        use_outline=use_outline,
        deterministic=deterministic
    )

    inputs = [{"query": q} for q in queries]
    rows, metrics = ev.run_on_records(inputs, gts=gts, print_first=print_first)
    final_pred = [r["pred"] for r in rows]
    return final_pred, metrics, rows

